
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpextensionInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpextension/create
	groupgo.POST("/tpextension/create", TpExtensionCreate)
	groupgo.POST("/tpextension/delete", TpExtensionDelete)
	groupgo.POST("/tpextension/update", TpExtensionUpdate)
	groupgo.POST("/tpextension/select", TpExtensionSelect)
}

// TpExtensionCreate ...
func TpExtensionCreate(c *gin.Context) {

	var param service.TpExtensionCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpExtensionService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

