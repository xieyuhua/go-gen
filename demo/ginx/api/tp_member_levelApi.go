
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpmemberlevelInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpmemberlevel/create
	groupgo.POST("/tpmemberlevel/create", TpMemberLevelCreate)
	groupgo.POST("/tpmemberlevel/delete", TpMemberLevelDelete)
	groupgo.POST("/tpmemberlevel/update", TpMemberLevelUpdate)
	groupgo.POST("/tpmemberlevel/select", TpMemberLevelSelect)
}

// TpMemberLevelCreate ...
func TpMemberLevelCreate(c *gin.Context) {

	var param service.TpMemberLevelCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpMemberLevelService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

