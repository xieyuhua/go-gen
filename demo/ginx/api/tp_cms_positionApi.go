
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpcmspositionInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpcmsposition/create
	groupgo.POST("/tpcmsposition/create", TpCmsPositionCreate)
	groupgo.POST("/tpcmsposition/delete", TpCmsPositionDelete)
	groupgo.POST("/tpcmsposition/update", TpCmsPositionUpdate)
	groupgo.POST("/tpcmsposition/select", TpCmsPositionSelect)
}

// TpCmsPositionCreate ...
func TpCmsPositionCreate(c *gin.Context) {

	var param service.TpCmsPositionCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpCmsPositionService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

