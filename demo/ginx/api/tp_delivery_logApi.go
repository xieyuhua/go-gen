
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpdeliverylogInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpdeliverylog/create
	groupgo.POST("/tpdeliverylog/create", TpDeliveryLogCreate)
	groupgo.POST("/tpdeliverylog/delete", TpDeliveryLogDelete)
	groupgo.POST("/tpdeliverylog/update", TpDeliveryLogUpdate)
	groupgo.POST("/tpdeliverylog/select", TpDeliveryLogSelect)
}

// TpDeliveryLogCreate ...
func TpDeliveryLogCreate(c *gin.Context) {

	var param service.TpDeliveryLogCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpDeliveryLogService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

