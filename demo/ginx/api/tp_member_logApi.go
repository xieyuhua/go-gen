
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpmemberlogInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpmemberlog/create
	groupgo.POST("/tpmemberlog/create", TpMemberLogCreate)
	groupgo.POST("/tpmemberlog/delete", TpMemberLogDelete)
	groupgo.POST("/tpmemberlog/update", TpMemberLogUpdate)
	groupgo.POST("/tpmemberlog/select", TpMemberLogSelect)
}

// TpMemberLogCreate ...
func TpMemberLogCreate(c *gin.Context) {

	var param service.TpMemberLogCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpMemberLogService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

