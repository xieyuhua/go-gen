
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func szyloginuserInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/szyloginuser/create
	groupgo.POST("/szyloginuser/create", SzyLoginUserCreate)
	groupgo.POST("/szyloginuser/delete", SzyLoginUserDelete)
	groupgo.POST("/szyloginuser/update", SzyLoginUserUpdate)
	groupgo.POST("/szyloginuser/select", SzyLoginUserSelect)
}

// SzyLoginUserCreate ...
func SzyLoginUserCreate(c *gin.Context) {

	var param service.SzyLoginUserCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.SzyLoginUserService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

