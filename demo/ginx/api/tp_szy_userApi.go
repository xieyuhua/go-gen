
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpszyuserInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpszyuser/create
	groupgo.POST("/tpszyuser/create", TpSzyUserCreate)
	groupgo.POST("/tpszyuser/delete", TpSzyUserDelete)
	groupgo.POST("/tpszyuser/update", TpSzyUserUpdate)
	groupgo.POST("/tpszyuser/select", TpSzyUserSelect)
}

// TpSzyUserCreate ...
func TpSzyUserCreate(c *gin.Context) {

	var param service.TpSzyUserCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpSzyUserService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

