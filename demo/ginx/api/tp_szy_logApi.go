
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpszylogInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpszylog/create
	groupgo.POST("/tpszylog/create", TpSzyLogCreate)
	groupgo.POST("/tpszylog/delete", TpSzyLogDelete)
	groupgo.POST("/tpszylog/update", TpSzyLogUpdate)
	groupgo.POST("/tpszylog/select", TpSzyLogSelect)
}

// TpSzyLogCreate ...
func TpSzyLogCreate(c *gin.Context) {

	var param service.TpSzyLogCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpSzyLogService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

