
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpmemberInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpmember/create
	groupgo.POST("/tpmember/create", TpMemberCreate)
	groupgo.POST("/tpmember/delete", TpMemberDelete)
	groupgo.POST("/tpmember/update", TpMemberUpdate)
	groupgo.POST("/tpmember/select", TpMemberSelect)
}

// TpMemberCreate ...
func TpMemberCreate(c *gin.Context) {

	var param service.TpMemberCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpMemberService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

