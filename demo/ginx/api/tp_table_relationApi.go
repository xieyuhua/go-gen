
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tptablerelationInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tptablerelation/create
	groupgo.POST("/tptablerelation/create", TpTableRelationCreate)
	groupgo.POST("/tptablerelation/delete", TpTableRelationDelete)
	groupgo.POST("/tptablerelation/update", TpTableRelationUpdate)
	groupgo.POST("/tptablerelation/select", TpTableRelationSelect)
}

// TpTableRelationCreate ...
func TpTableRelationCreate(c *gin.Context) {

	var param service.TpTableRelationCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpTableRelationService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

