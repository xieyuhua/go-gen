
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpadminuserInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpadminuser/create
	groupgo.POST("/tpadminuser/create", TpAdminUserCreate)
	groupgo.POST("/tpadminuser/delete", TpAdminUserDelete)
	groupgo.POST("/tpadminuser/update", TpAdminUserUpdate)
	groupgo.POST("/tpadminuser/select", TpAdminUserSelect)
}

// TpAdminUserCreate ...
func TpAdminUserCreate(c *gin.Context) {

	var param service.TpAdminUserCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAdminUserService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

