
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpadminroleInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpadminrole/create
	groupgo.POST("/tpadminrole/create", TpAdminRoleCreate)
	groupgo.POST("/tpadminrole/delete", TpAdminRoleDelete)
	groupgo.POST("/tpadminrole/update", TpAdminRoleUpdate)
	groupgo.POST("/tpadminrole/select", TpAdminRoleSelect)
}

// TpAdminRoleCreate ...
func TpAdminRoleCreate(c *gin.Context) {

	var param service.TpAdminRoleCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAdminRoleService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

