
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpshippingareaInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpshippingarea/create
	groupgo.POST("/tpshippingarea/create", TpShippingAreaCreate)
	groupgo.POST("/tpshippingarea/delete", TpShippingAreaDelete)
	groupgo.POST("/tpshippingarea/update", TpShippingAreaUpdate)
	groupgo.POST("/tpshippingarea/select", TpShippingAreaSelect)
}

// TpShippingAreaCreate ...
func TpShippingAreaCreate(c *gin.Context) {

	var param service.TpShippingAreaCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpShippingAreaService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

