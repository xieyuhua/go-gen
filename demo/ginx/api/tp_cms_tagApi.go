
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpcmstagInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpcmstag/create
	groupgo.POST("/tpcmstag/create", TpCmsTagCreate)
	groupgo.POST("/tpcmstag/delete", TpCmsTagDelete)
	groupgo.POST("/tpcmstag/update", TpCmsTagUpdate)
	groupgo.POST("/tpcmstag/select", TpCmsTagSelect)
}

// TpCmsTagCreate ...
func TpCmsTagCreate(c *gin.Context) {

	var param service.TpCmsTagCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpCmsTagService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

