
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpshoptagInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpshoptag/create
	groupgo.POST("/tpshoptag/create", TpShopTagCreate)
	groupgo.POST("/tpshoptag/delete", TpShopTagDelete)
	groupgo.POST("/tpshoptag/update", TpShopTagUpdate)
	groupgo.POST("/tpshoptag/select", TpShopTagSelect)
}

// TpShopTagCreate ...
func TpShopTagCreate(c *gin.Context) {

	var param service.TpShopTagCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpShopTagService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

