
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpadminpermissionInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpadminpermission/create
	groupgo.POST("/tpadminpermission/create", TpAdminPermissionCreate)
	groupgo.POST("/tpadminpermission/delete", TpAdminPermissionDelete)
	groupgo.POST("/tpadminpermission/update", TpAdminPermissionUpdate)
	groupgo.POST("/tpadminpermission/select", TpAdminPermissionSelect)
}

// TpAdminPermissionCreate ...
func TpAdminPermissionCreate(c *gin.Context) {

	var param service.TpAdminPermissionCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAdminPermissionService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

