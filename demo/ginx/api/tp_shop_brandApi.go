
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpshopbrandInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpshopbrand/create
	groupgo.POST("/tpshopbrand/create", TpShopBrandCreate)
	groupgo.POST("/tpshopbrand/delete", TpShopBrandDelete)
	groupgo.POST("/tpshopbrand/update", TpShopBrandUpdate)
	groupgo.POST("/tpshopbrand/select", TpShopBrandSelect)
}

// TpShopBrandCreate ...
func TpShopBrandCreate(c *gin.Context) {

	var param service.TpShopBrandCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpShopBrandService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

