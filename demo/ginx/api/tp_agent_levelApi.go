
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpagentlevelInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpagentlevel/create
	groupgo.POST("/tpagentlevel/create", TpAgentLevelCreate)
	groupgo.POST("/tpagentlevel/delete", TpAgentLevelDelete)
	groupgo.POST("/tpagentlevel/update", TpAgentLevelUpdate)
	groupgo.POST("/tpagentlevel/select", TpAgentLevelSelect)
}

// TpAgentLevelCreate ...
func TpAgentLevelCreate(c *gin.Context) {

	var param service.TpAgentLevelCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAgentLevelService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

