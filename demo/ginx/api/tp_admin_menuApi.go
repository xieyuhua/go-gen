
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpadminmenuInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpadminmenu/create
	groupgo.POST("/tpadminmenu/create", TpAdminMenuCreate)
	groupgo.POST("/tpadminmenu/delete", TpAdminMenuDelete)
	groupgo.POST("/tpadminmenu/update", TpAdminMenuUpdate)
	groupgo.POST("/tpadminmenu/select", TpAdminMenuSelect)
}

// TpAdminMenuCreate ...
func TpAdminMenuCreate(c *gin.Context) {

	var param service.TpAdminMenuCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAdminMenuService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

