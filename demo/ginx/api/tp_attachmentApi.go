
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpattachmentInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpattachment/create
	groupgo.POST("/tpattachment/create", TpAttachmentCreate)
	groupgo.POST("/tpattachment/delete", TpAttachmentDelete)
	groupgo.POST("/tpattachment/update", TpAttachmentUpdate)
	groupgo.POST("/tpattachment/select", TpAttachmentSelect)
}

// TpAttachmentCreate ...
func TpAttachmentCreate(c *gin.Context) {

	var param service.TpAttachmentCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAttachmentService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

