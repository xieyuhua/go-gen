
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpshopcategoryInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpshopcategory/create
	groupgo.POST("/tpshopcategory/create", TpShopCategoryCreate)
	groupgo.POST("/tpshopcategory/delete", TpShopCategoryDelete)
	groupgo.POST("/tpshopcategory/update", TpShopCategoryUpdate)
	groupgo.POST("/tpshopcategory/select", TpShopCategorySelect)
}

// TpShopCategoryCreate ...
func TpShopCategoryCreate(c *gin.Context) {

	var param service.TpShopCategoryCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpShopCategoryService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

