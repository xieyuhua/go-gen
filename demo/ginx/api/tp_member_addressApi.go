
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpmemberaddresInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpmemberaddres/create
	groupgo.POST("/tpmemberaddres/create", TpMemberAddresCreate)
	groupgo.POST("/tpmemberaddres/delete", TpMemberAddresDelete)
	groupgo.POST("/tpmemberaddres/update", TpMemberAddresUpdate)
	groupgo.POST("/tpmemberaddres/select", TpMemberAddresSelect)
}

// TpMemberAddresCreate ...
func TpMemberAddresCreate(c *gin.Context) {

	var param service.TpMemberAddresCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpMemberAddresService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

