
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpadmingroupInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpadmingroup/create
	groupgo.POST("/tpadmingroup/create", TpAdminGroupCreate)
	groupgo.POST("/tpadmingroup/delete", TpAdminGroupDelete)
	groupgo.POST("/tpadmingroup/update", TpAdminGroupUpdate)
	groupgo.POST("/tpadmingroup/select", TpAdminGroupSelect)
}

// TpAdminGroupCreate ...
func TpAdminGroupCreate(c *gin.Context) {

	var param service.TpAdminGroupCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAdminGroupService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

