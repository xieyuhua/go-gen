
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpcmscategoryInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpcmscategory/create
	groupgo.POST("/tpcmscategory/create", TpCmsCategoryCreate)
	groupgo.POST("/tpcmscategory/delete", TpCmsCategoryDelete)
	groupgo.POST("/tpcmscategory/update", TpCmsCategoryUpdate)
	groupgo.POST("/tpcmscategory/select", TpCmsCategorySelect)
}

// TpCmsCategoryCreate ...
func TpCmsCategoryCreate(c *gin.Context) {

	var param service.TpCmsCategoryCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpCmsCategoryService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

