
package api
import (
	"ginx/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func tpareacityInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/tpareacity/create
	groupgo.POST("/tpareacity/create", TpAreacityCreate)
	groupgo.POST("/tpareacity/delete", TpAreacityDelete)
	groupgo.POST("/tpareacity/update", TpAreacityUpdate)
	groupgo.POST("/tpareacity/select", TpAreacitySelect)
}

// TpAreacityCreate ...
func TpAreacityCreate(c *gin.Context) {

	var param service.TpAreacityCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.TpAreacityService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

