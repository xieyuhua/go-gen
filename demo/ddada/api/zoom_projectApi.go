
package api
import (
	"ddada/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func zoomprojectInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/zoomproject/create
	groupgo.POST("/zoomproject/create", ZoomProjectCreate)
	groupgo.POST("/zoomproject/delete", ZoomProjectDelete)
	groupgo.POST("/zoomproject/update", ZoomProjectUpdate)
	groupgo.POST("/zoomproject/select", ZoomProjectSelect)
	groupgo.GET("/zoomproject/all", ZoomProjectAll)
}

// ZoomProjectCreate ...
func ZoomProjectCreate(c *gin.Context) {

	var param service.ZoomProjectCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.ZoomProjectService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

