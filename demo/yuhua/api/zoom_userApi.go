
package api
import (
	"yuhua/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func zoomuserInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/zoomuser/create
	groupgo.POST("/zoomuser/create", ZoomUserCreate)
	groupgo.POST("/zoomuser/delete", ZoomUserDelete)
	groupgo.POST("/zoomuser/update", ZoomUserUpdate)
	groupgo.POST("/zoomuser/select", ZoomUserSelect)
}

// ZoomUserCreate ...
func ZoomUserCreate(c *gin.Context) {

	var param service.ZoomUserCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.ZoomUserService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

