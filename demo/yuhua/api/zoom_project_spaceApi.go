
package api
import (
	"yuhua/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func zoomprojectspaceInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/zoomprojectspace/create
	groupgo.POST("/zoomprojectspace/create", ZoomProjectSpaceCreate)
	groupgo.POST("/zoomprojectspace/delete", ZoomProjectSpaceDelete)
	groupgo.POST("/zoomprojectspace/update", ZoomProjectSpaceUpdate)
	groupgo.POST("/zoomprojectspace/select", ZoomProjectSpaceSelect)
}

// ZoomProjectSpaceCreate ...
func ZoomProjectSpaceCreate(c *gin.Context) {

	var param service.ZoomProjectSpaceCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.ZoomProjectSpaceService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

