
package api
import (
	"yuhua/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func zoomusertokenInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/zoomusertoken/create
	groupgo.POST("/zoomusertoken/create", ZoomUserTokenCreate)
	groupgo.POST("/zoomusertoken/delete", ZoomUserTokenDelete)
	groupgo.POST("/zoomusertoken/update", ZoomUserTokenUpdate)
	groupgo.POST("/zoomusertoken/select", ZoomUserTokenSelect)
	groupgo.GET("/zoomusertoken/all", ZoomUserTokenAll)
	
}

// ZoomUserTokenCreate ...
func ZoomUserTokenCreate(c *gin.Context) {

	var param service.ZoomUserTokenCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.ZoomUserTokenService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

