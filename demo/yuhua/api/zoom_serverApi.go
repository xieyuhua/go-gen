
package api
import (
	"yuhua/service"
	"github.com/gin-gonic/gin"
	"net/http"
)

func zoomserverInit(groupgo *gin.RouterGroup) {
	 
	// 所有api的开头 都加上/go  例如 /go/zoomserver/create
	groupgo.POST("/zoomserver/create", ZoomServerCreate)
	groupgo.POST("/zoomserver/delete", ZoomServerDelete)
	groupgo.POST("/zoomserver/update", ZoomServerUpdate)
	groupgo.POST("/zoomserver/select", ZoomServerSelect)
}

// ZoomServerCreate ...
func ZoomServerCreate(c *gin.Context) {

	var param service.ZoomServerCreateParam
	//解析参数
	err := c.ShouldBind(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	ser := &service.ZoomServerService{}
	back, err := ser.Create(&param)
	if err != nil {
		reply := NewReplyError(err.Error())
		c.JSON(http.StatusOK, reply)
		return
	}
	reply := NewReplyOk()
	reply.Data = back
	c.JSON(http.StatusOK, reply)
	return
}

